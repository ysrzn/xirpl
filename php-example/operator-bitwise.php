<?php
$a = 70;
$b = 17;

//bitwise AND
$c = $a & $b;
echo "$a & $b = $c";
echo "<br>";

//bitwise OR
$c = $a | $b;
echo "$a | $b = $c";
echo "<br>";

//bitwise XOR
$c = $a ^ $b;
echo "$a ^ $b = $c";
echo "<br>";

//bitwise left
$c = $a << $b;
echo "$a << $b = $c";
echo "<br>";

//shift rigth
$c = $a >> $b;
echo "$a >> $b = $c";
echo "<br>";

?>